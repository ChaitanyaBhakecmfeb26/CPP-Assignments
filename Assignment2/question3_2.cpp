// Two differences between pointer and reference
//  Why references cannot be reseated but pointers can
//  Why references cannot be NULL 

// 1
// A pointer stores the memory address of a variable.
// Pointer can be reassigned (reseated) to point to another variable.
// A reference is an alias (another name) for an existing variable.
// Reference cannot be reseated after initialization.


// 2
// A reference must be initialized when it is created, and after that it always refers to the same variable.

// Pointers store addresses, so we can change the address they store.

// 3
// A reference must always refer to a valid variable.
// Because a reference must always be linked to an existing object.
